    //Evento sobre boton: se abre el dialogo para escoger opciones
    private void jButtonRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRutaActionPerformed
        //Se abre un dialogo 
        JFileChooser fileChooser = new JFileChooser();
        //el padre es el propio panel
        int resultado = fileChooser.showOpenDialog(this);
        //Comprobacion si el usuario seleccion√≥ algo
        //si no, no debe hacer nada
        if (resultado == JFileChooser.APPROVE_OPTION) {
            //entonces se recoge el resultado
            File file = fileChooser.getSelectedFile();
            //e muestra la nruta en el cuadro de texto
            jTextFieldRuta.setText(file.getAbsolutePath());
        }
    }//GEN-LAST:event_jButtonRutaActionPerformed

    
    //Metodo Publico - que devuelva un objeto tipo ImagenFondo
    //que devuelva recogiendo los valores de los componentes: ruta de imagen y opacidad
    public ImagenFondo getSelectedValue() {
        //A partir de la ruta recogido en el TextField, se construye un 
        //objeto de tipo file
        File f = new File(jTextFieldRuta.getText());
        //recogemos dato de opacidad del Slider - getValue() que devuelve un int 
        //para cambiarlo a Float del atributo opacidad - dividimos entre 100
        Float opacidad = jSliderOpacidad.getValue()/100f;   
        return new ImagenFondo(f, opacidad);      
    }

